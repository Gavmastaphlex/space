function changeImage1() {

        if (document.getElementById("process-icon-1").src == "../images/process/icons/icon1.png") 
        {
            document.getElementById("process-icon-1").src = "../images/process/icons/icon1_.png";
        }
        else 
        {
            document.getElementById("process-icon-1").src = "../images/process/icons/icon1.png";
        }
    }